                <p>Buying instead of renting needs to make sense financially. To help you decide, play with Zillow’s Buy vs. Rent calculator to see how many years it will take before the cost of buying equals the cost of renting. It’s called the breakeven horizon, and it varies by area of the country. If you plan to stay in your home past your breakeven horizon, then buying makes financial sense. If you think you’ll move earlier, then renting may be the way to go.</p>
