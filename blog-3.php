            <h3>Are you financially ready?</h3>
            <p>Buying a house involves raising a down payment and paying a monthly mortgage, which lasts anywhere from 5 to 30 years, depending on the home loan you can afford and are offered. There are other costs as well, but let’s focus on the big money.</p>

            <p>Down payment: It’s the lump sum you’ll pay upfront that funds equity in the property and proves to lenders that you’ve got skin in this homeowner game. Down payments vary. In the go-go days that led up to the housing collapse, some lenders dismissed the down payment altogether – and we see how well that ended. Today, 20 percent is preferred and often gets you the best rates, but some loans allow down payments as low as 3 percent. Sometimes parents or friends can offer help with the down payment. If you have a choice, take a gift rather than a loan, not only for obvious reasons, but because lenders will add that debt to other monthly obligations and potential mortgage payments to determine your debt-to-income ratio, which generally can’t top 43 percent to qualify for a home loan.

            Monthly mortgage payments: This is what you’ll pay each month. In most cases, a mortgage includes the loan principal and interest (both amortized over the life of the loan) plus homeowners insurance and property taxes (pro-rated). When credit was tight, getting a mortgage at any rate was reserved for only the most credit-worthy borrowers. Things have loosened, but lenders still want to know that you’re a responsible, gainfully employed and credit-worthy candidate.</p>

            <h3>Are you emotionally ready?</h3>
            <p>Owning a home is a huge commitment so before jumping in, consider if you are ready to make lots of decisions, from picking an agent to picking paint colors. Are you confident enough to select a neighborhood where you’ll want to stay for a while? And are you up for devoting the time and attention to maintaining a home? Weekends will disappear under chores like pulling weeds, cleaning gutters, shoveling snow, sealing counters and decks, and on and on. Taking care of your biggest investment can be gratifying but only if you’re ready.</p>
            
