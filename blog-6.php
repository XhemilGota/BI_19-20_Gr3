                <h2><mark>What Type of Mortgage Is Best for You?</mark></h2>

               <h3>Fixed-rate loan or adjustable-rate loan</h3>
                <p>When deciding on a loan type, one of the main factors to consider is the type of interest rate you are comfortable with: fixed or adjustable. Here’s a look at each of these loan types, with pros and cons to consider.</p>

                <h3>FIXED-RATE MORTGAGES</h3>
                <p>This is the traditional workhorse mortgage. It gets paid off over a set amount of time (10, 15, 20 or 30 years) at a specific interest rate. A 30-year fixed is the most common. Market rates may rise and fall, but your interest rate won’t budge.<br>

                Why would you want a fixed-rate loan? One word: security. You won’t have to worry about a rising interest rate. Your monthly payments may fluctuate a bit with property tax and insurance rates, but they’ll be fairly stable. If rates drop significantly, you can always refinance. The shorter the loan term, the lower the interest rate. For example, a 15-year fixed will have a lower interest rate than a 30-year fixed.<br>

                Why wouldn’t you want a fixed rate? If you plan on moving in five or even 10 years, you may be better off with a lower adjustable rate. It’s the conservative choice for the long term, which means you will pay for the security it promises.</p><br>

                <h3>ADJUSTABLE-RATE MORTGAGES (ARMS)</h3>
                <p>You’ll get a lower initial interest rate compared to a fixed-rate mortgage but it won’t necessarily stay there. The interest rate fluctuates with an indexed rate plus a set margin. But don’t worry — you won’t be faced with huge monthly fluctuations. Adjustment intervals are predetermined and there are minimum and maximum rate caps to limit the size of the adjustment.<br>

                Why would you want an ARM? Lower rates are an immediate appeal. If you aren’t planning on staying in your home for long, or if you plan to refinance in the near term, an ARM is something you should consider. You can qualify for a higher loan amount with an ARM (due to the lower initial interest rate). Annual ARMs have historically outperformed fixed rate loans.<br>

                Why wouldn’t you want an ARM? You have to assume worst-case scenario here. Rates may increase after the adjustment period. If you don’t think you’ll save enough upfront to offset the future rate increase, or if you don’t want to risk having to refinance, think twice.<br>

                What should I look for? Look carefully at the frequency of adjustments. You’ll get a lower starting rate with more frequent adjustments but also more uncertainty. Check the payments at the upper limit of your cap and make sure you can afford them. Relying on a refinance to bail you out is a big risk.</p>
