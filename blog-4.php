                    <h3>What exactly is a mortgage?</h3>
                    <p>It’s a loan with your house and land used as collateral. If you don’t pay back the loan, the lender will foreclose. That doesn’t mean the bank owns the house until you pay it off. It means they’ve got a lien against the property. A lien is the right to take possession of someone else’s property, in this case your home, until a debt is paid off. So you really are a homeowner even if you have a mortgage. You just own a home with a lien. Zillow’s Mortgage Learning Center offers extensive information about mortgages and is a great resource for anyone in the market for a home loan.</p>

                    <h3>I can borrow as much as I want, right?</h3>
                    <p>This seemed to be the thinking a few years ago, and things didn’t turn out very well. When you borrow more than you can realistically pay, that’s a sub-prime mortgage. Banks sold a lot of those to people who assumed the housing market would keep rising like gangbusters. Their home values would go up, giving them nearly instant equity and they could refinance quickly at a lower rate or sell the home for a quick profit. Lenders sold these loan products because they were making the same bet, and interest rates are always higher on sub-prime loans. Even if some ended up in foreclosure, the lenders would still make a tidy profit. Unfortunately, it was a bad bet for almost everyone.<br>

                    As the housing market shows more upward movement, the temptation to borrow more than you can afford becomes enticing. That’s why it’s important to really look at how much you can spend. Your mortgage payment should be comfortable even if it’s a stretch, not a weight that drags you down each month. The lender will look at your income, debt and savings, and is required by federal regulation to demonstrate your ability to repay a loan. So while that determines how much you can borrow, it isn’t necessarily what you can afford.</p><br>

                    <p>You can find a lender on Zillow to learn how much you can borrow. And you can use Zillow’s affordability calculator to estimate what you can afford.  But you should go a step further and figure out what you can be comfortable with. Is travel a passion? Do you like spending a fair amount on dining out or other entertainment? The lender won’t factor biannual vacations or a craving for high-end restaurants into their calculations, so you have to. Fortunately, that’s easy enough with tools that help you calculate your monthly payment as well as estimate what you should be able to afford given your existing income and debts. Chances are, even after the sub-prime crisis, a lender will be willing to offer you a bigger mortgage than you think you can afford. Only you can know how much you are willing to set aside for a mortgage payment each month.</p>

                    <h3>How do I know if I’ll even qualify for a mortgage?</h3>
                    <p>A few years ago (see above), if you were breathing it seemed like you could find a mortgage. Things are a little bit tighter now. The biggest factor is your debt to income ratio. It’s your minimum monthly debt divided by your monthly income. But don’t worry. You don’t have to do the math! There’s a handy DTI calculator that will figure it out for you and estimate how much you’re likely to qualify for.</p>
